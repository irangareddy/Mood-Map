import Foundation

/// An enumeration representing different time periods of the day.
///
/// Use the `TimeOfDay` enum to represent morning, afternoon, evening, and night time periods.
public enum TimeOfDay: String {
    /// The morning time period.
    case morning = "Morning"

    /// The afternoon time period.
    case afternoon = "Afternoon"

    /// The evening time period.
    case evening = "Evening"

    /// The night time period.
    case night = "Night"
}
